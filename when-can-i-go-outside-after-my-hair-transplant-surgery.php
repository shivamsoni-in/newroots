                            <li class="text-bold patient">
                                <b>Minimize Sweating</b> : Sweating can be detrimental to the healing process after a hair transplant surgery. Engaging in strenuous activities that induce excessive sweating should be avoided for at least two weeks post-surgery.
                            </li>
                            
                            <li class="text-bold patient">
                                <b>Avoid Touching or Scratching</b> : It's essential to resist the temptation to touch or scratch the transplanted area. Doing so can disrupt the healing process and potentially damage the grafts.
                            </li>
                            
