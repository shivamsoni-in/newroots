                        of the 5α-reductase enzymes that convert testosterone into DHT. It inhibits all 
                        three forms of 5α-reductase, and can decrease DHT levels in the blood by up to 
                        98%. This is in contrast to finasteride, which is similarly an irreversible 
                        inhibitor of 5α-reductase but only inhibits the type II and III isoenzymes. 
