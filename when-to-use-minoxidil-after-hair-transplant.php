                        <h3 class="subtitle approach mt-2">Wait for the healing process to finish </h3>
                        <p>
                            After a hair transplant, it's crucial to allow your scalp to fully heal before you start using minoxidil.
                        </p>
                        <p>
                            It's recommended that you wait at least two weeks after your surgery before you begin using this product.
                        </p>
