                        <h2 class="subtitle approach mt-2" style="font-size:28px;"> What is a hair transplant ? </h2>
                        
                        <img src="assets/img/blog/Hair-Transplant-in-Mumbai.jpg" class="img-fluid" alt="Hair Transplant in Mumbai">
                        
                        <p>
                            A hair transplant is a cosmetic procedure that involves the transfer of hair follicles from a donor site (typically the back of the head) to a balding or thinning area (the recipient site) on the scalp. 
                        </p>
                        <p>
                            The goal of a hair transplant is to restore hair growth in the recipient area and achieve a fuller head of hair.
